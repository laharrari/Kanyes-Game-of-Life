gameBoard.prototype.update = function () {
    // If game is running, keep stepping
    if (!GAME_ENGINE.pause && this.counter > this.counterLimit) {
        this.nextStep();
        this.counter = 0;
    } else {
        this.counter += GAME_ENGINE.clockTick;
    }
}

gameBoard.prototype.draw = function () {
    if (GAME_ENGINE.menuFlag) { // At menu display menu image
        GAME_ENGINE.ctx.drawImage(AM.getAsset("./img/the_life_of_cells.png"), 0, 0);
    } else {
        // If user clicks on menu then begin Kanye's Game of Life
        for (let i = 0; i < this.width; i++) {
            for (let j = 0; j < this.height; j++) {
                if (this.board[i][j] === 1) {
                    GAME_ENGINE.ctx.fillStyle = "black";
                    GAME_ENGINE.ctx.fillRect(j * TILE_SIZE, i * TILE_SIZE, TILE_SIZE, TILE_SIZE);
                } else {
                    GAME_ENGINE.ctx.fillStyle = "#f58c59";
                    GAME_ENGINE.ctx.fillRect(j * TILE_SIZE, i * TILE_SIZE, TILE_SIZE, TILE_SIZE);
                }
                if (GAME_ENGINE.debug) {
                    GAME_ENGINE.ctx.strokeStyle = "#fdb897";
                    GAME_ENGINE.ctx.strokeRect(j * TILE_SIZE, i * TILE_SIZE, TILE_SIZE, TILE_SIZE);
                }
            }
        }
        this.music.play();
    }
}

gameBoard.prototype.clearBoard = function () {
    for (var i = 0; i < this.width; i++) {
        for (var j = 0; j < this.width; j++) {
            this.board[i][j] = 0;
        }
    }
}

gameBoard.prototype.nextStep = function () {
    var newBoard = [];

    for (var s = 0; s < this.width; s++) {
        newBoard[s] = this.board[s].slice();
    }

    for (var i = 0; i < this.width; i++) {
        for (var j = 0; j < this.height; j++) {
            var aliveNeighbors = 0;
            for (var x = -1; x <= 1; x++) {
                for (var y = -1; y <= 1; y++) {
                    if (x + i >= 0 && x + i < this.width && y + j >= 0 && y + j < this.height) {
                        var num = this.board[x + i][y + j];
                        aliveNeighbors += num;
                    }
                }
            }

            aliveNeighbors -= this.board[i][j];
            if (this.board[i][j] === 1 && aliveNeighbors < this.populationFloor ||
                this.board[i][j] === 1 && aliveNeighbors > this.populationCeiling) {
                newBoard[i][j] = 0;
            } else if ((this.board[i][j] === 0 && aliveNeighbors === this.reproduction)) {
                newBoard[i][j] = 1;
            }
        }
    }

    this.board = newBoard;
}

