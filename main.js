AM.downloadAll(function () {
    var canvas = document.getElementById("gameWorld");
    var ctx = canvas.getContext("2d");
    document.body.style.backgroundColor = "#fdb897";
    canvasWidth = canvas.width;
    canvasHeight = canvas.height;
    canvas.focus();


    // For adjusting volume with volume slider
    var volumeSlider = document.getElementById("volumeSlider");
    volumeSlider.addEventListener("change", function () {
        BOARD_AREA.music.volume = volumeSlider.value;
        BOARD_AREA.myCurrentVolume = BOARD_AREA.music.volume;
        BOARD_AREA.myIsMute = false;
        muteButton.innerHTML = "\"Mute\"";
    }, false);

    // For muting audio with mute button
    var muteButton = document.getElementById("muteButton");
    muteButton.addEventListener("click", function () {
        if (BOARD_AREA.myIsMute) {
            BOARD_AREA.music.volume = BOARD_AREA.myCurrentVolume;
            volumeSlider.value = BOARD_AREA.myCurrentVolume;
            BOARD_AREA.myCurrentVolume = BOARD_AREA.music.volume;
            BOARD_AREA.myIsMute = false;
            muteButton.innerHTML = "\"Mute\"";
        } else {
            BOARD_AREA.myCurrentVolume = BOARD_AREA.music.volume;
            BOARD_AREA.music.volume = 0.0;
            muteButton.innerHTML = "\"Unmute\"";
            volumeSlider.value = 0.0;
            BOARD_AREA.myIsMute = true;
        }
    });

    BOARD_AREA.music.addEventListener("ended", function() {
        BOARD_AREA.music.currentTime = 0;
        BOARD_AREA.music.play();
    });

    GAME_ENGINE.init(ctx);
    GAME_ENGINE.start();
    GAME_ENGINE.addEntity(BOARD_AREA);
});